enum SyncFailure {
  /// Синхронизация уже выполняется
  alreadyInProgress,

  /// Конфликт данных при синхронизации
  conflict,

  /// Нет данных для синхронизации
  noData,

  /// Данные устарели — требуется полная синхронизация
  dataOutdated,

  /// Ошибка частичной синхронизации
  partialSync,

  /// Синхронизация отменена
  cancelled,

  /// Таймаут синхронизации
  timeout,

  /// Сервер синхронизации недоступен
  serverUnavailable,

  /// Несовместимая версия данных
  versionMismatch,

  /// Превышен лимит данных для синхронизации
  dataLimitExceeded,
}
